export const TAG36H11_COUNT = TAG36H11_CODES.length;

/**
 * Compute Hamming distance (popcount of XOR) between two 36-bit values.
 */
export function hammingDistance(code1: number, code2: number): number {
  const xor = code1 ^ code2;
  // Count set bits using builtin
  return popcount(xor);
}

/**
 * Fast popcount for 32-bit integers.
 */
function popcount(x: number): number {
  x = x - ((x >>> 1) & 0x55555555);
  x = (x & 0x33333333) + ((x >>> 2) & 0x33333333);
  x = (x + (x >>> 4)) & 0x0f0f0f0f;
  x = x + (x >>> 8);
  x = x + (x >>> 16);
  return x & 0x3f;
}

// Type for pattern with possible unknown cells
export type TagPattern = (0 | 1 | -1)[];

/**
 * Convert 6x6 binary pattern (row-major) to 36-bit integer.
 * Bit 0 = top-left cell (row 0, col 0)
 */
export function patternToCode(pattern: TagPattern): number {
  if (pattern.length !== 36) return -1;
  let code = 0;
  for (let i = 0; i < 36; i++) {
    if (pattern[i] === 1) {
      code |= (1 << i);
    }
  }
  return code;
}

/**
 * Match detected pattern against tag36h11 dictionary.
 * Returns tag ID if found within error threshold, or -1 if no match.
 *
 * @param pattern 6x6 binary pattern (36 bits)
 * @param maxError Maximum Hamming distance to consider a match
 * @returns Tag family ID (0-586) or -1
 */
export function decodeTag36h11(
  pattern: TagPattern | null,
  maxError: number = 5,
): number {
  if (!pattern || pattern.length !== 36) return -1;

  const code = patternToCode(pattern);
  if (code === -1) return -1;

  // Count unknown cells
  const unknownCount = pattern.filter(v => v === -1).length;
  if (unknownCount > maxError * 2) return -1; // Too many unknown cells

  // Find best match in dictionary
  let bestId = -1;
  let bestDist = maxError + 1;

  for (let id = 0; id < TAG36H11_COUNT; id++) {
    const dist = hammingDistance(code, TAG36H11_CODES[id]);
    if (dist < bestDist) {
      bestDist = dist;
      bestId = id;
      if (bestDist === 0) break; // Exact match
    }
  }

  return bestId;
}

/**
 * Rotate a 6x6 pattern 90 degrees clockwise.
 */
export function rotatePattern(pattern: TagPattern): TagPattern {
  const result: TagPattern = new Array(36) as TagPattern;
  for (let row = 0; row < 6; row++) {
    for (let col = 0; col < 6; col++) {
      const srcIdx = row * 6 + col;
      const dstRow = col;
      const dstCol = 5 - row;
      const dstIdx = dstRow * 6 + dstCol;
      result[dstIdx] = pattern[srcIdx];
    }
  }
  return result;
}

/**
 * Decode tag with all 4 rotations, return best match.
 */
export function decodeTag36h11AnyRotation(
  pattern: TagPattern | null,
  maxError: number = 5,
): { id: number; rotation: number } | null {
  if (!pattern) return null;

  // Try each rotation
  let currentPattern = [...pattern];
  for (let r = 0; r < 4; r++) {
    const id = decodeTag36h11(currentPattern, maxError);
    if (id !== -1) {
      return { id, rotation: r };
    }
    currentPattern = rotatePattern(currentPattern);
  }

  return null;
}